package com.sachini.booking.dao;

public class WebScraped {

    private String vehicleName;
    private String monthlyRate;
    private String weeklyRate;
    private String millagePerDay;

    public String getVehicleName() {
        return vehicleName;
    }

    public void setVehicleName(String vehicleName) {
        this.vehicleName = vehicleName;
    }

    public String getMonthlyRate() {
        return monthlyRate;
    }

    public void setMonthlyRate(String monthlyRate) {
        this.monthlyRate = monthlyRate;
    }

    public String getWeeklyRate() {
        return weeklyRate;
    }

    public void setWeeklyRate(String weeklyRate) {
        this.weeklyRate = weeklyRate;
    }

    public String getMillagePerDay() {
        return millagePerDay;
    }

    public void setMillagePerDay(String millagePerDay) {
        this.millagePerDay = millagePerDay;
    }
}
